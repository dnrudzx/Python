import string

print(string.digits)                   #0-9 숫자
print(string.ascii_lowercase)          #알파벳 소문자
print(string.ascii_uppercase)          #알파벳 대문자
print(string.ascii_letters)            #알파벳 소문자-대문자

print(string.capwords('abcd'))         #단어단위 첫글자 대문자로

print(string.hexdigits)                #16진법 나열
print(string.octdigits)                #8진법 나열